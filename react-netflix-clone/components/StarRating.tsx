const EmptyStar = () => (
  <svg className="w-4 h-4 text-gray-400" fill="currentColor" viewBox="0 0 20 20">
    <path d="M9.049 2.927c.3-.921 1.603-.921 1.902 0l1.07 3.292a1 1 0 00.95.69h3.462c.969 0 1.371 1.24.588 1.81l-2.8 2.034a1 1 0 00-.364 1.118l1.07 3.292c.3.921-.755 1.688-1.54 1.118l-2.8-2.034a1 1 0 00-1.175 0l-2.8 2.034c-.784.57-1.838-.197-1.539-1.118l1.07-3.292a1 1 0 00-.364-1.118L2.98 8.72c-.783-.57-.38-1.81.588-1.81h3.461a1 1 0 00.951-.69l1.07-3.292z" />
  </svg>
);

interface StarRatingProps {
  rating: number; // Rating out of 10
}

const StarRating: React.FC<StarRatingProps> = ({ rating }) => {
  const stars = [];
  const ratingOutOf5 = rating / 2;
  const fullStars = Math.floor(ratingOutOf5);
  const halfStar = ratingOutOf5 - fullStars >= 0.5;
  const emptyStars = 5 - fullStars - (halfStar ? 1 : 0);

  for (let i = 0; i < fullStars; i++) {
    stars.push(<FullStar key={`full-${i}`} />);
  }
  if (halfStar) {
    stars.push(<HalfStar key="half" />);
  }
  for (let i = 0; i < emptyStars; i++) {
    stars.push(<EmptyStar key={`empty-${i}`} />);
  }

  return (
    <div className="flex items-center">
      {stars}
      <span className="ml-2 text-xs text-gray-300 font-semibold">{rating.toFixed(1)}</span>
    </div>
  );
};

export default StarRating;
